	private static string decode(int id) {
		int remainder1; 
		int quotient1 = Math.DivRem(id, Convert.ToInt32(0x1000000), out remainder1);
		int remainder2; 
		int quotient2 = Math.DivRem(remainder1, Convert.ToInt32(0x10000), out remainder2);
		int remainder3; 
		int quotient3 = Math.DivRem(remainder2, Convert.ToInt32(0x100), out remainder3);
		return string.Concat (Convert.ToChar (quotient1), Convert.ToChar (quotient2), Convert.ToChar (quotient3), Convert.ToChar (remainder3)).Trim();
	}

	private static Vector3[] reducedRepresentation(PDB_molecule mol, out string[] labels) {
		List<Vector3> atomCoords = new List<Vector3>();
		List<string> atomLabels = new List<string>();

		// Iterate through all amino acids
		// and for each amino acid iterate through all its atoms
		// and find the ones to be considered and/or calculate pseudo atoms 
		for (int i = 0; i != mol.aminoAcidsAtomIds.Count; ++i) {
			string aaName = mol.aminoAcidsNames[i].Trim ();
			string oneletterAAName = threeLetterToOneLetterAminoAcid[aaName];
			int[] ids = mol.aminoAcidsAtomIds [i];
			// The two pseudo atoms (geometric centres)
			Vector3 gc1 = Vector3.zero;
			Vector3 gc2 = Vector3.zero;
			// Number of atoms considered for the pseudoatoms
			int numAtomsForGC1 = 0;
			int numAtomsForGC2 = 0;
			// if large amino acid
			if (smallAminoAcids.Contains(aaName)) {
				for (int j = 0; j != ids.Length; ++j) {
					string atomName = decode(mol.names[ids[j]]);
					//discard hydrogens
					if (!atomName.StartsWith("H")) {
						//if backbone nitrogen or backbone oxygen
						if (atomName == "N" || atomName == "O") {
							atomCoords.Add(mol.atom_centres[ids[j]]);
							atomLabels.Add ("Ba"+atomName);
						// if not any of the remaining backbone atoms
						} else if (atomName != "CA" && atomName != "C") {
							gc1 = gc1 + mol.atom_centres[ids[j]];
							++numAtomsForGC1;
						}
					}
				}
				if (numAtomsForGC1 > 0) {
					gc1 /= numAtomsForGC1;
					atomCoords.Add(gc1);
					atomLabels.Add (oneletterAAName);
				}
			// if small amino acid
			} else if (largeAminoAcids.Contains(aaName)) {
				for (int j = 0; j != ids.Length; ++j) {
					string atomName = decode(mol.names[ids[j]]);
					//discard hydrogens
					if (!atomName.StartsWith("H")) {
						//if backbone nitrogen or backbone oxygen
						if (atomName == "N" || atomName == "O") {
							atomCoords.Add(mol.atom_centres[ids[j]]);
							atomLabels.Add ("Ba"+atomName);
						// if not any of the remaining backbone atoms
						} else if (atomName != "CA" && atomName != "C") {
							if (atomName == "CB" || atomName == "CG") {
								gc1 = gc1 + mol.atom_centres[ids[j]];
								++numAtomsForGC1;
							} else {
								gc2 = gc2 + mol.atom_centres[ids[j]];
								++numAtomsForGC2;
							}
						}
					}
				}				
				if (numAtomsForGC1 > 0) {
					gc1 /= numAtomsForGC1;
					atomCoords.Add(gc1);
					atomLabels.Add (oneletterAAName+"1");
				}
				if (numAtomsForGC2 > 0) {
					gc2 /= numAtomsForGC2;
					atomCoords.Add(gc2);
					atomLabels.Add (oneletterAAName+"2");
				}
			// if GLY
			// NOTE: this "if" is not redundant. It ensures that only standard amino acids
			// are considered.
			} else if (aaName == "GLY") {
				//Debug.Log (threeLetterToOneLetterAminoAcid[name]);
				for (int j = 0; j != ids.Length; ++j) {
					string atomName = decode(mol.names[ids[j]]);
					//discard hydrogens
					if (!atomName.StartsWith("H")) {
						//if backbone nitrogen or backbone oxygen
						if (atomName == "N" || atomName == "O") {
							atomCoords.Add(mol.atom_centres[ids[j]]);
							atomLabels.Add ("Ba"+atomName);
						// if CA
						} else if (atomName == "CA") {
							atomCoords.Add(mol.atom_centres[ids[j]]);
							atomLabels.Add ("Gca");
						}
					}
				}
			}
		}

		labels = atomLabels.ToArray ();
		return atomCoords.ToArray ();
	}

	public PDB_score(PDB_molecule receptor, Transform recT, PDB_molecule ligand, Transform ligT) {
		// calculate Emin and Rmin^2 values and add them to the scoring matrix
		List<string> keys = new List<string>(scoringMatrix.Keys);
		foreach (string key in keys) {
			//TODO: some optimization here is possible but written in a clean way to understand
			// dvd: stands for dividend
			float attrDvd = scoringMatrix[key][1] * Mathf.Pow (scoringMatrix[key][0], 6.0f);
			float repDvd = scoringMatrix[key][1] * Mathf.Pow (scoringMatrix[key][0], 8.0f);
			scoringMatrix[key][3] = (-27.0f * Mathf.Pow (attrDvd, 4.0f)) / (256.0f * Mathf.Pow (repDvd, 3.0f));
			scoringMatrix[key][4] = (4.0f * repDvd) / (3.0f * attrDvd);
		}
		//foreach (string key in scoringMatrix.Keys) {
		//	if (scoringMatrix[key][4] > cutoff*0.5f) {
		//		Debug.Log (scoringMatrix[key][4]);
		//	}
		//}

		// generate the reduced representations
		recAtomCoords = reducedRepresentation (receptor, out recAtomLabels);
		ligAtomCoords = reducedRepresentation (ligand, out ligAtomLabels);
		this.recT = recT;
		this.ligT = ligT;
		Debug.Log ("Rec: from " + receptor.names.Length + " to " + recAtomCoords.Length);
		Debug.Log ("Lig: from " + ligand.names.Length + " to " + ligAtomCoords.Length);
	}

	public void calcScore() {
		elecScore = 0f;
		vdwScore = 0f;
		int charge1 = 0;
		int charge2 = 0;
		// Check the distance between all ligand-receptor atom pairs.
		Matrix4x4 transfMat = recT.worldToLocalMatrix * ligT.localToWorldMatrix;
		for (int j = 0; j != ligAtomCoords.Length; ++j) {
			Vector3 lCoords = transfMat.MultiplyPoint3x4 (ligAtomCoords[j]);
			for (int i = 0; i != recAtomCoords.Length; ++i) {
				// This actually is distance^2
				float distance2 = (lCoords - recAtomCoords[i]).sqrMagnitude;
				//Scoring function
				if (distance2 <= cutoff) {
					if (distance2 < 0.001) {
						distance2 = 0.001f;
					}
					string key = recAtomLabels[j] + "_" + ligAtomLabels[j];

					charges.TryGetValue(ligAtomLabels[j], out charge1);
					charges.TryGetValue(recAtomLabels[j], out charge2);
					float elec = (332.053986f * charge1 * charge2) / (15.0f * distance2);

					// dvd: stands for dividend
					// dvs: stands for divisor
					// TODO: optimization
					float attrDvd = scoringMatrix[key][1] * Mathf.Pow (scoringMatrix[key][0], 6.0f);
					float repDvd = scoringMatrix[key][1] * Mathf.Pow (scoringMatrix[key][0], 8.0f);
					float attrDvs = Mathf.Pow (distance2, 3.0f);
					float repDvs = Mathf.Pow (distance2, 4.0f);
					float vdw = (repDvd/repDvs) - (attrDvd/attrDvs);
					//if repulsive
					if (scoringMatrix[key][2] < 0) {
						//switch between minimum or saddle point
						if (distance2 > scoringMatrix[key][4]) {
							vdw *= scoringMatrix[key][2];
						} else {
							vdw += (scoringMatrix[key][2]-1)*scoringMatrix[key][3];
						}
					}
					elecScore += elec;
					vdwScore += vdw;

				}
			}
		}

		score = elecScore + vdwScore;
	}
	
