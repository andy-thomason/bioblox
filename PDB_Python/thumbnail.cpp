struct vector3 {
  float x, y, z;
  
  vector3() : x(0), y(0), z(0) {}
  vector3(float x, float y, float z) : x(x), y(y), z(z) {}

  float length() const { return std::sqrt(x*x + y*y + z*z); }

  static vector3 lerp(vector3 a, vector3 b, float lambda) {
    float rl = 1.0f - lambda;
    return vector3(a.x * rl + b.x * lambda, a.y * rl + b.y * lambda, a.z * rl + b.z * lambda);
  }
  
  vector3 operator *(float v) { return vector3(x*v, y*v, z*v); }
  
  vector3 normalised() { float rl = 1.0f / length(); return *this * rl; }
};

struct colour {
  float r, g, b, a;
  
  colour() : r(1), g(1), b(1), a(1) {}
  colour(float r, float g, float b, float a) : r(r), g(g), b(b), a(a) {}

  static colour lerp(colour a, colour b, float lambda) {
    float rl = 1.0f - lambda;
    return colour(a.r * rl + b.r * lambda, a.g * rl + b.g * lambda, a.b * rl + b.b * lambda, a.a * rl + b.a * lambda);
  }
};

/// Helper to construct marching cubes meshes.
class marching_cubes {
public:
  std::vector<vector3> vertices;
  std::vector<vector3> normals;
  std::vector<colour> colours;
  std::vector<int> indices;


  // Given a 3D lattice of values (mc_values) generate triangles where values transition from positive to negative.
  marching_cubes(int x0, int y0, int z0, int xdim, int ydim, int zdim, float grid_spacing, const float *mc_values, const vector3 *mc_normals, const colour *mc_colours) {
    //float rgs = 1.0f / grid_spacing;

    // This reproduced the vertex order of Paul Bourke's (borrowed) table.
    // The indices in edge_indices have the following offsets.
    //
    //     7 6   y   z
    // 3 2 4 5   | /
    // 0 1       0 - x
    //

    // Now build the marching cubes triangles.
    // Each cube owns three edges 0->1 0->3 0->4
    std::vector<int> edge_indices(xdim*ydim*zdim*3);

    // We store three indices per cube for the edges closest to vertex 0
    // All other indices can be derived from adjacent cubes.
    // This gives a single index offset value for each edge.
    // There are twelve edges here because we consider adjacent cubes also.
    int dx = 3;
    int dy = xdim * 3;
    int dz = xdim * ydim * 3;
    int edge_offsets[] = {
      0*dx+0*dy+0*dz+0,  // 0,1,
      1*dx+0*dy+0*dz+1,  // 1,2,
      0*dx+1*dy+0*dz+0,  // 2,3,
      0*dx+0*dy+0*dz+1,  // 3,0,
      0*dx+0*dy+1*dz+0,  // 4,5,
      1*dx+0*dy+1*dz+1,  // 5,6,
      0*dx+1*dy+1*dz+0,  // 6,7,
      0*dx+0*dy+1*dz+1,  // 7,4,
      0*dx+0*dy+0*dz+2,  // 0,4,
      1*dx+0*dy+0*dz+2,  // 1,5,
      1*dx+1*dy+0*dz+2,  // 2,6,
      0*dx+1*dy+0*dz+2,  // 3,7
    };
        
    // two passes for vertices (pass = 0 sizes the array)
    for (size_t i = 0; i != edge_indices.size(); ++i) {
      edge_indices [i] = -1;
    }

    for (int k = 0; k != zdim; ++k) {
      for (int j = 0; j != ydim; ++j) {
        for (int i = 0; i != xdim; ++i) {
          int idx = (k * ydim + j) * xdim + i;
          float v0 = mc_values [idx];
          // x edges
          if (i != xdim-1) {
            float v1 = mc_values [idx + 1];
            if (v0 * v1 < 0) {
              float lambda = v0 / (v0 - v1);
              edge_indices[idx*3+0] = (int)vertices.size();
              vertices.push_back(vector3(float(x0 + i + lambda), float(y0 + j), float(z0 + k)) * grid_spacing);
              normals.push_back(vector3::lerp (mc_normals[idx], mc_normals[idx+1], lambda).normalised());
              colours.push_back(colour::lerp (mc_colours[idx], mc_colours[idx+1], lambda));
            }
          }

          // y edges
          if (j != ydim-1) {
            float v1 = mc_values [idx + xdim];
            if (v0 * v1 < 0) {
              float lambda = v0 / (v0 - v1);
              edge_indices[idx*3+1] = (int)vertices.size();
              vertices.push_back(vector3(float(x0 + i), float(y0 + j + lambda), float(z0 + k)) * grid_spacing);
              normals.push_back(vector3::lerp (mc_normals[idx], mc_normals[idx+xdim], lambda).normalised());
              colours.push_back(colour::lerp (mc_colours[idx], mc_colours[idx+xdim], lambda));
            }
          }

          // z edges
          if (k != zdim-1) {
            float v1 = mc_values [idx + xdim*ydim];
            if (v0 * v1 < 0) {
              float lambda = v0 / (v0 - v1);
              edge_indices[idx*3+2] = (int)vertices.size();
              vertices.push_back(vector3(x0 + i, y0 + j, z0 + k + lambda) * grid_spacing);
              normals.push_back(vector3::lerp (mc_normals[idx], mc_normals[idx+xdim*ydim], lambda).normalised());
              colours.push_back(colour::lerp (mc_colours[idx], mc_colours[idx+xdim*ydim], lambda));
            }
          }
        }
      }
    }

    // loop over all cubes
    for (int k = 0; k != zdim-1; ++k) {
      for (int j = 0; j != ydim-1; ++j) {
        for (int i = 0; i != xdim-1; ++i) {
          int idx = (k * ydim + j) * xdim + i;
          //vector3 pos0 = new vector3 (x0 + i, y0 + j, z0 + k);

          // Mask of vertices outside the isosurface (values are negative)
          // Example:
          //   00000001 means only vertex 0 is outside the surface.
          //   10000000 means only vertex 7 is outside the surface.
          //   11111111 all vertices are outside the surface.
          int mask =
            (mc_values [idx] < 0 ? 1 << 0 : 0) |
            (mc_values [idx + 1] < 0 ? 1 << 1 : 0) |
            (mc_values [idx + 1 + xdim] < 0 ? 1 << 2 : 0) |
            (mc_values [idx + xdim] < 0 ? 1 << 3 : 0) |
          
            (mc_values [idx + xdim * ydim] < 0 ? 1 << 4 : 0) |
            (mc_values [idx + 1 + xdim * ydim] < 0 ? 1 << 5 : 0) |
            (mc_values [idx + 1 + xdim + xdim * ydim] < 0 ? 1 << 6 : 0) |
            (mc_values [idx + xdim + xdim * ydim] < 0 ? 1 << 7 : 0
          );

          int off = mask * 16;
          while (mc_triangles[off] != -1) {
            indices.push_back(edge_indices [idx*3 + edge_offsets [(int)mc_triangles [off + 0]]]);
            indices.push_back(edge_indices [idx*3 + edge_offsets [(int)mc_triangles [off + 1]]]);
            indices.push_back(edge_indices [idx*3 + edge_offsets [(int)mc_triangles [off + 2]]]);
            off += 3;
          }
        }
      }
    }
  }
};


